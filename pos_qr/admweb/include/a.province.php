function Arrays_province()
{
	$f = PATH_ADMIN . '/include/province.json';
	$myfile = fopen($f, "r") or die("Unable to open file!");
	$jsOn = fread($myfile,filesize($f));
	fclose($myfile);
	$a = json_decode($jsOn, true);
	$aRe = array();
	foreach ($a as $k => $v) {
		$aRe[$v['province_code']]['name'] = $v['province'];
		$aRe[$v['province_code']]['amphoe'][$v['amphoe_code']]['name'] = $v['amphoe'];
		$aRe[$v['province_code']]['amphoe'][$v['amphoe_code']]['district'][$v['district_code']] = $v['district'];
	}
	
	return $aRe;
}

